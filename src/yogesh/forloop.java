package yogesh;

public class forloop {

	public static void main(String[] args) {
		
		int a=1;
		int b=100;
		 
		for(int z=a;z<=b;z++) {
			if(z%2==0) {
				System.out.println("even"+z);
			}else {
				System.out.println("odd"+z);
			}
		}
		
		while(a<=10) {
			System.out.println(a);
			a=a+1;
		}
		
		
		
		
		
		
		
		
		
	}

}
